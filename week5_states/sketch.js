let sceneStates = Object.freeze({
  BACKGROUND: 0,
  FIRST: 1,
  SECOND: 2,
  TOP: 3,
});

let currentState = sceneStates.BACKGROUND;

let keyOn = false;

function setup() {
  createCanvas(1275, 1650);
}

function draw() {
  drawScene();
  checkTransition();
  keyOn = false;
}

function drawScene() {
  switch (currentState) {
    case sceneStates.BACKGROUND:
      drawBackground();
      break;
    case sceneStates.FIRST:
      drawFirst();
      break;
    case sceneStates.SECOND:
      drawSecond();
      break;
    case sceneStates.TOP:
      drawTop();
      break;
  }
}

function checkTransition() {
  switch(currentState) {
    case sceneStates.BACKGROUND:
      checkState();
      break;
    case sceneStates.FIRST:
      checkState();
      break;
    case sceneStates.SECOND:
      checkState();
      break;
    case sceneStates.TOP:
      checkState();
      break;
  }
}

function setupScene() {
  switch(currentState) {
    case sceneStates.BACKGROUND:
      break;
    case sceneStates.FIRST:
      break;
    case sceneStates.SECOND:
      break;
  }
}

function keyPressed() {
  keyOn = true;
}

